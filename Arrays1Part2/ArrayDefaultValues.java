package Arrays1Part2;

public class ArrayDefaultValues {
    public static void main(String[] args) {
        byte bt[] = new byte[5];
        short st[] = new short[5];
        int in[] = new int[5];
        long ln[] = new long[5];
        float ft[] = new float[5];
        double db[] = new double[5];
        boolean bl[] = new boolean[5];
        char ch[] = new char[5];
        String str[] = new String[5];

        System.out.println("byte :" + bt);
        System.out.println("byte default:" + bt[0]);
        System.out.println("short :"+st);
        System.out.println("short default:"+st[0]);
        System.out.println("int :" + in);
        System.out.println("int default:" + in[0]);
        System.out.println("long :" +ln);
        System.out.println("long default:" +ln[0]);
        System.out.println("float :"+ft);
        System.out.println("float default:"+ft[0]);
        System.out.println("double :"+db);
        System.out.println("double default:"+db[0]);
        System.out.println("boolean :"+bl);
        System.out.println("boolean default:"+bl[0]);
        System.out.println("char :" + ch);
        System.out.println("char default:"+ch[0]);
        System.out.println("String :" + str);
        System.out.println("String default:" + str[0]);
    }
}
